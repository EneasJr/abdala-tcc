    private boolean validateForms() {
        for (Component c : this.pnDadosPessoais.getComponents()) {
            if (c instanceof JTextField) {
                JTextField jtf = (JTextField) c;
                if (jtf.equals(txtCelular) || jtf.equals(txtCodigo) || jtf.equals(txtData)) {
                    continue;
                }
                if (jtf.getText().equals("")) {
                    return false;
                }
            }
        }

        for (Component c : this.pnEndereco.getComponents()) {
            if (c instanceof JTextField) {
                JTextField jtf = (JTextField) c;
                if (jtf.equals(txtEndereco2)) {
                    continue;
                }
                if (jtf.getText().equals("")) {
                    return false;
                }
            }
        }
        return true;
    }
